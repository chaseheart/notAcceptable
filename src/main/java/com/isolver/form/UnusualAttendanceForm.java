package com.isolver.form;


/**
 * @author IS1907005
 * @date 2019/11/18
 * @class AbnormalAttendanceForm.java
 */
public class UnusualAttendanceForm {
	/** 。非正常考勤时间 **/
	private String date;
	/** 。非正常考勤类型 **/
	private String type;
	/** 。实体单据有无 **/
	private Boolean hasDocument;
	/** 。非正常考勤原因 **/
	private String reason;
	/** 。考勤务实ID **/
	private String servicePerformance;
	/** 。审批者 **/
	private String assigner;

	/**
	 * @return the unusualAttendanceDate
	 */
	public String getDate() {
		return date;
	}

	/**
	 * @param unusualAttendanceDate the unusualAttendanceDate to set
	 */
	public void setDate(String date) {
		this.date = date;
	}

	/**
	 * @return the unusualAttendanceType
	 */
	public String getType() {
		return type;
	}

	/**
	 * @param unusualAttendanceType the unusualAttendanceType to set
	 */
	public void setType(String type) {
		this.type = type;
	}

	/**
	 * @return the hasDocument
	 */
	public Boolean getHasDocument() {
		return hasDocument;
	}

	/**
	 * @param hasDocument the hasDocument to set
	 */
	public void setHasDocument(Boolean hasDocument) {
		this.hasDocument = hasDocument;
	}

	/**
	 * @return the reason
	 */
	public String getReason() {
		return reason;
	}

	/**
	 * @param reason the reason to set
	 */
	public void setReason(String reason) {
		this.reason = reason;
	}

	/**
	 * @return the servicePerformance
	 */
	public String getServicePerformance() {
		return servicePerformance;
	}

	/**
	 * @param servicePerformance the servicePerformance to set
	 */
	public void setServicePerformance(String servicePerformance) {
		this.servicePerformance = servicePerformance;
	}

	/**
	 * @return the assigner
	 */
	public String getAssigner() {
		return assigner;
	}

	/**
	 * @param assigner the assigner to set
	 */
	public void setAssigner(String assigner) {
		this.assigner = assigner;
	}

}
