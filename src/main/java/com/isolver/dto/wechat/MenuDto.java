package com.isolver.dto.wechat;

/**
 * @author IS1907005
 * @date 2019/08/19
 * @class LoginDto.java
 */
public class MenuDto {
	/** id **/ 
	private String id;
	/** 菜单名 **/
	private String menuName;
	/** 菜单icon **/
	private String menuIcon;
	/** 菜单url **/
	private String menuUrl;
	/** 是否存在 **/ 
	private String isExisted;
	
	public MenuDto() {
		
	}
	
	public MenuDto(String id, String menuName, String menuIcon, String menuUrl, String isExisted) {
		super();
		this.id = id;
		this.menuName = menuName;
		this.menuIcon = menuIcon;
		this.menuUrl = menuUrl;
		this.isExisted = isExisted;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getMenuName() {
		return menuName;
	}

	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}

	public String getMenuIcon() {
		return menuIcon;
	}

	public void setMenuIcon(String menuIcon) {
		this.menuIcon = menuIcon;
	}

	public String getMenuUrl() {
		return menuUrl;
	}

	public void setMenuUrl(String menuUrl) {
		this.menuUrl = menuUrl;
	}

	public String getIsExisted() {
		return isExisted;
	}

	public void setIsExisted(String isExisted) {
		this.isExisted = isExisted;
	}
	
	
}
