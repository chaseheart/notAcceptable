package com.isolver.dto.wechat;

import java.util.Date;

public class WechatVacationRecordDto {

	/** 年休(天) **/
	private Integer annualLeave;
	/** 调休(时) **/
	private Integer paidLeave;
	/** 日期 **/
	private Date date;

	/**
	 * @return the annualLeave
	 */
	public Integer getAnnualLeave() {
		return annualLeave;
	}

	/**
	 * @param annualLeave the annualLeave to set
	 */
	public void setAnnualLeave(Integer annualLeave) {
		this.annualLeave = annualLeave;
	}

	/**
	 * @return the paidLeave
	 */
	public Integer getPaidLeave() {
		return paidLeave;
	}

	/**
	 * @param paidLeave the paidLeave to set
	 */
	public void setPaidLeave(Integer paidLeave) {
		this.paidLeave = paidLeave;
	}

	/**
	 * @return the date
	 */
	public Date getDate() {
		return date;
	}

	/**
	 * @param date the date to set
	 */
	public void setDate(Date date) {
		this.date = date;
	}

	public WechatVacationRecordDto() {
		super();
	}

	/**
	 * WechatVacationRecordDto.java
	 * 
	 * @param annualLeave
	 * @param paidLeave
	 * @param date
	 * 
	 */
	public WechatVacationRecordDto(Integer annualLeave, Integer paidLeave, Date date) {
		super();
		this.annualLeave = annualLeave;
		this.paidLeave = paidLeave;
		this.date = date;
	}
}
